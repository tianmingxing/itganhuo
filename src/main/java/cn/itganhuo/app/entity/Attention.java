package cn.itganhuo.app.entity;

import java.io.Serializable;

/**
 * 关注数据封装类：共有两种关注类型，一是可以关注标签，二是可以关注其它用户，三是收藏文章。
 * Created by 小兴 on 2015/3/22.
 */
public class Attention implements Serializable {

    private static final long serialVersionUID = -7494419104468878801L;
    private Integer id;
    private Integer type;
    private Integer userId;
    private Integer labelId;
    private Integer byUserId;
    private Integer articleId;
    private String postDate;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getLabelId() {
        return labelId;
    }

    public void setLabelId(Integer labelId) {
        this.labelId = labelId;
    }

    public Integer getByUserId() {
        return byUserId;
    }

    public void setByUserId(Integer byUserId) {
        this.byUserId = byUserId;
    }

    public String getPostDate() {
        return postDate;
    }

    public void setPostDate(String postDate) {
        this.postDate = postDate;
    }

    public Integer getArticleId() {
        return articleId;
    }

    public void setArticleId(Integer articleId) {
        this.articleId = articleId;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Attention attention = (Attention) o;

        if (id != null ? !id.equals(attention.id) : attention.id != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        return id != null ? id.hashCode() : 0;
    }

    @Override
    public String toString() {
        return "Attention{" +
                "id=" + id +
                ", type=" + type +
                ", userId=" + userId +
                ", labelId=" + labelId +
                ", byUserId=" + byUserId +
                ", articleId=" + articleId +
                ", postDate='" + postDate + '\'' +
                '}';
    }
}
